#Teste de igualdade e de não igualdade com strings
pizza = 'calabresa'
print("Esta pizza é de 'calabresa'?")
print(pizza == 'calabresa')
print("Esta pizza é de 'muçarela'?")
print(pizza == 'muçarela')

#Teste utilizando a função lower()
nome = 'Ana'
print("O nome é 'ana'?")
print(nome.lower() == 'ana')
print("O nome é 'jana'?")
print(nome.lower() == 'jana')
