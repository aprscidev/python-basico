#Solicitando a quantidade de litros de combustível e distância percorrida
litros_combustivel = int(input('Digite a quantidade de combustível em litros: '))
distancia = int(input('Digite a distância percorrida em quilômetros: '))

#Calculando o consumo médio
consumo_medio = distancia / litros_combustivel

#Saida
print(f'O consumo médio é de {consumo_medio} km/l.')

